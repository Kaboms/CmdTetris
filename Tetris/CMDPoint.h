#pragma once
//------------------------------------------------------------------------
#include <stdint.h>
//------------------------------------------------------------------------
struct CMDPoint
{
	int8_t x, y;

	CMDPoint()
	{
		x = 0;
		y = 0;
	}
	//------------------------------------------------------------------------

	CMDPoint(int8_t x, int8_t y)
	{
		this->x = x;
		this->y = y;
	}
	//------------------------------------------------------------------------

	CMDPoint operator+(CMDPoint& p)
	{
		CMDPoint point(this->x, this->y);

		point.x += p.x;
		point.y += p.y;

		return point;
	}
};
